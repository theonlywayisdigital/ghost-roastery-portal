"use client";

import { useState } from "react";
import { sectionCatalog } from "@/lib/website-sections/catalog";
import type { SectionType, SectionCategory } from "@/lib/website-sections/types";

interface SectionCatalogModalProps {
  open: boolean;
  onClose: () => void;
  onAdd: (type: SectionType) => void;
}

const categories: SectionCategory[] = [
  "Hero",
  "Products",
  "Content",
  "Social",
  "Forms",
  "Advanced",
];

export function SectionCatalogModal({ open, onClose, onAdd }: SectionCatalogModalProps) {
  const [activeCategory, setActiveCategory] = useState<SectionCategory | "All">("All");

  if (!open) return null;

  const filtered =
    activeCategory === "All"
      ? sectionCatalog
      : sectionCatalog.filter((s) => s.category === activeCategory);

  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center">
      {/* Backdrop */}
      <div className="absolute inset-0 bg-black/40" onClick={onClose} />

      {/* Modal */}
      <div className="relative bg-white rounded-xl shadow-xl w-full max-w-2xl max-h-[80vh] flex flex-col">
        <div className="flex items-center justify-between px-6 py-4 border-b border-neutral-200">
          <h2 className="text-lg font-semibold text-neutral-900">Add Section</h2>
          <button
            onClick={onClose}
            className="w-8 h-8 rounded-lg flex items-center justify-center text-neutral-400 hover:bg-neutral-100 hover:text-neutral-600"
          >
            <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M6 18L18 6M6 6l12 12" />
            </svg>
          </button>
        </div>

        {/* Category tabs */}
        <div className="flex items-center gap-1 px-6 py-3 border-b border-neutral-100 overflow-x-auto">
          <CategoryTab
            label="All"
            active={activeCategory === "All"}
            onClick={() => setActiveCategory("All")}
          />
          {categories.map((cat) => (
            <CategoryTab
              key={cat}
              label={cat}
              active={activeCategory === cat}
              onClick={() => setActiveCategory(cat)}
            />
          ))}
        </div>

        {/* Grid */}
        <div className="flex-1 overflow-y-auto p-6">
          <div className="grid grid-cols-2 gap-3">
            {filtered.map((entry) => (
              <button
                key={entry.type}
                onClick={() => {
                  onAdd(entry.type);
                  onClose();
                }}
                className="text-left rounded-lg border border-neutral-200 p-4 hover:border-blue-400 hover:bg-blue-50/50 transition-colors group"
              >
                <div className="flex items-start gap-3">
                  <div className="w-9 h-9 rounded-lg bg-neutral-100 flex items-center justify-center text-neutral-500 group-hover:bg-blue-100 group-hover:text-blue-600 transition-colors shrink-0">
                    <SectionIcon name={entry.icon} />
                  </div>
                  <div>
                    <p className="text-sm font-semibold text-neutral-900">{entry.label}</p>
                    <p className="text-xs text-neutral-400 mt-0.5 leading-relaxed">
                      {entry.description}
                    </p>
                  </div>
                </div>
              </button>
            ))}
          </div>
        </div>
      </div>
    </div>
  );
}

function CategoryTab({
  label,
  active,
  onClick,
}: {
  label: string;
  active: boolean;
  onClick: () => void;
}) {
  return (
    <button
      onClick={onClick}
      className={`px-3 py-1.5 rounded-full text-xs font-medium whitespace-nowrap transition-colors ${
        active
          ? "bg-blue-600 text-white"
          : "bg-neutral-100 text-neutral-600 hover:bg-neutral-200"
      }`}
    >
      {label}
    </button>
  );
}

